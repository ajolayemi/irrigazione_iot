create table
  public.sectors (
    id bigint generated by default as identity,
    name character varying not null,
    area double precision not null,
    num_of_plants double precision not null,
    water_consumption_per_hour double precision not null,
    irrigation_system_type character varying not null,
    irrigation_source character varying not null,
    turn_on_command character varying not null,
    turn_off_command character varying not null,
    notes text null,
    total_consumption double precision GENERATED ALWAYS AS (num_of_plants * water_consumption_per_hour) STORED,
    specie_id bigint not null,
    variety_id bigint not null,
    company_id bigint not null,
    created_at timestamp with time zone not null default now(),
    updated_at timestamp with time zone not null,
    constraint sectors_pkey primary key (id),
    constraint sectors_turn_on_command_key unique (turn_on_command),
    constraint sectors_turn_off_command_key unique (turn_off_command),
    constraint public_sectors_specie_id_fkey foreign key (specie_id) references species (id) on update restrict on delete cascade,
    constraint public_sectors_variety_id_fkey foreign key (variety_id) references varieties (id) on update restrict on delete cascade,
    constraint public_sectors_company_id_fkey foreign key (company_id) references companies (id) on update restrict on delete cascade
  ) tablespace pg_default;